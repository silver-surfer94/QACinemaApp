    private int standard;
    private int OAP;
    private int student;
    private int child;
    private int total;
    private int discount;
    

    
    public void setStandard() {
        standard = (int)standardQty.getValue();
    }
    
    public void setOap() {
        OAP = (int)oapQty.getValue();
    }
    
    public void setStudent() {
        student = (int)studentQty.getValue();
    }
    
    public void setChild() {
        child = (int)childQty.getValue();
    }
    
    public int getStandard() {
        return standard;
    }
    
    public int getOap() {
        return OAP;
    }
    
    public int getStudent() {
        return student;
    }
    
    public int getChild() {
        return child;
    }
    
    public void setTotal() {
        total = standard * 8 + OAP * 6 + student * 6 + child * 4 - getDiscount();
    }
    
    public int getTotal() {
        return total;
    }
    
    public void setDiscount() {
        if (DayCB.getSelectedItem().toString() == "Wednesday") {
            discount = (standard + OAP + student + child) * 2;
        } else {
            discount = 0;
        }
    }
    
    public int getDiscount() {
        return discount;
    }
    
    

