    private void checkDay(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_checkDay
        // TODO add your handling code here:
        if (DayCB.getSelectedItem().toString() == "Wednesday") {
           DiscLbl.setVisible(true);
           DiscLbl2.setVisible(true);
           setStandard();
            setOap();
            setStudent();
            setChild();
            setDiscount();
            setTotal();
            DiscLbl2.setText(String.valueOf("-£" + getDiscount()));
            TotalField.setText(String.valueOf("£" + getTotal()));
        } else {
           DiscLbl.setVisible(false);
           DiscLbl2.setVisible(false);
           setStandard();
            setOap();
            setStudent();
            setChild();
            setDiscount();
            setTotal();
            DiscLbl2.setText(String.valueOf("-£" + getDiscount()));
            TotalField.setText(String.valueOf("£" + getTotal()));
        }
    }//GEN-LAST:event_checkDay

